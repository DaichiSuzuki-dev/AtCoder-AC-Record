using System;

class C
{
    static void Main()
    {
        var N = int.Parse(Console.ReadLine());
        var ans= 0;
        for (int i = 1; i <= N; i++)
        {
            if (i.ToString().Contains("7") || Convert.ToString(i, 8).Contains("7"))
            {
                continue;
            }
            ans++;
        }

        Console.Write(ans);
    }
}